package java_basico;

/**
 * heloworld
 */
public class heloworld {

    public static void main(String[] args) {
        
        int a = 10;
        int b = 90;
        int soma = a + b;
        int subtracao = a - b;
        int produto = a * b;
        int divisao = a / b;

        System.out.println("soma = "+ soma);
        System.out.println("Subtração = " + subtracao);
        System.out.println("Produto = " + produto);
        System.out.println("Divisão = " + divisao);
        
    }
}